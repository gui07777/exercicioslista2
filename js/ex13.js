var numero = parseInt(prompt("Digite um número de 1 a 7:"));

if (numero === 1) {
 alert("1 - Domingo");
} else if (numero === 2) {
  alert("2 - Segunda-feira");
} else if (numero === 3) {
  alert("3 - Terça-feira");
} else if (numero === 4) {
  alert("4 - Quarta-feira");
} else if (numero === 5) {
  alert("5 - Quinta-feira");
} else if (numero === 6) {
  alert("6 - Sexta-feira");
} else if (numero === 7) {
  alert("7 - Sábado");
} else {
  alert("Valor inválido");
}